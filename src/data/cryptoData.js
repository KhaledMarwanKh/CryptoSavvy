export function updateCryptoTable(prevCryptoData) {
  const newCryptoData = [];

  const pos = ((Math.round(Math.random())) ? 1 : -1);

  const randomNumber = Math.random();

  for (let i of prevCryptoData) {
    let price = i.price + ((randomNumber * i.price).toFixed(2) + 1000.00) * pos;
    let change24h = i.change24h + ((randomNumber * 10).toFixed(2)) * pos;
    let change7d = i.change7d + ((randomNumber * 10).toFixed(2)) * pos;
    let volume = i.volume + ((randomNumber * i.volume).toFixed(2) + 1000.00) * pos;
    let marketCap = i.marketCap + ((randomNumber * i.marketCap).toFixed(2) + 1000.00) * pos;

    newCryptoData.push({ ...i, price, volume, change24h, change7d, marketCap })
  }

  return newCryptoData;

}

