// Generate orderbook data
export const generateOrderBookData = (basePrice) => {
  const bids = [];
  const asks = [];
  const currentPrice = basePrice;

  // Generate 15 bid orders (buy orders below current price)
  for (let i = 0; i < 15; i++) {
    const price = currentPrice - (i + 1) * 50 - Math.random() * 30;
    const quantity = (Math.random() * 2 + 0.1).toFixed(4);
    const amount = (price * quantity).toFixed(2);
    const total = bids.length > 0
      ? (parseFloat(bids[bids.length - 1].total) + parseFloat(amount)).toFixed(2)
      : amount;

    bids.push({
      price: price.toFixed(2),
      quantity,
      amount,
      total
    });
  }

  // Generate 15 ask orders (sell orders above current price)
  for (let i = 0; i < 15; i++) {
    const price = currentPrice + (i + 1) * 50 + Math.random() * 30;
    const quantity = (Math.random() * 2 + 0.1).toFixed(4);
    const amount = (price * quantity).toFixed(2);
    const total = asks.length > 0
      ? (parseFloat(asks[asks.length - 1].total) + parseFloat(amount)).toFixed(2)
      : amount;

    asks.push({
      price: price.toFixed(2),
      quantity,
      amount,
      total
    });
  }

  return { bids, asks };
};

