export const formatCurrency = (amount) => {
  // Use 'compact' notation for large amounts for better fit in cards
  if (amount > 1e6) {
    return new Intl.NumberFormat('en-US', { notation: 'compact', maximumFractionDigits: 1 }).format(amount);
  }
  const formatted = new Intl.NumberFormat('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(amount);
  return formatted;
};

export const formatLargeNumber = (num) => {
  if (num >= 1e12) return '$' + (num / 1e12).toFixed(2) + 'T';
  if (num >= 1e9) return '$' + (num / 1e9).toFixed(2) + 'B';
  if (num >= 1e6) return '$' + (num / 1e6).toFixed(2) + 'M';
  return '$' + formatCurrency(num);
};

export function generateChartData(days = 30) {
  const data = [];
  const now = Date.now();
  const dayMs = 24 * 60 * 60 * 1000;
  let basePrice = 60000 + Math.random() * 10000;

  for (let i = days; i >= 0; i--) {
    const time = Math.floor((now - i * dayMs) / 1000);
    const volatility = 0.02;
    const trend = Math.sin(i / 10) * 0.01;
    const change = (Math.random() - 0.5) * volatility + trend;

    basePrice *= (1 + change);

    const open = basePrice;
    const close = basePrice * (1 + (Math.random() - 0.5) * 0.01);
    const high = Math.max(open, close) * (1 + Math.random() * 0.01);
    const low = Math.min(open, close) * (1 - Math.random() * 0.01);

    data.push({
      time,
      open,
      high,
      low,
      close,
      value: close
    });
  }

  return data;
}

// Generate candlestick data
export const generateCandlestickData = (basePrice = 0) => {
  const data = [];
  const days = 30;
  const now = new Date();

  for (let i = days; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(date.getDate() - i);

    const open = basePrice + (Math.random() - 0.5) * 2000;
    const close = open + (Math.random() - 0.5) * 1500;
    const high = Math.max(open, close) + Math.random() * 800;
    const low = Math.min(open, close) - Math.random() * 800;

    data.push({
      time: Math.floor(date.getTime() / 1000),
      open: parseFloat(open.toFixed(2)),
      high: parseFloat(high.toFixed(2)),
      low: parseFloat(low.toFixed(2)),
      close: parseFloat(close.toFixed(2))
    });

    basePrice = close;
  }

  return data;
};

